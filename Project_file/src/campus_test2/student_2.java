    public void creat_account(){
        
        //user details
        String user_name1 = Ltext1.getText();
        String user_name2 = Ltext2.getText();
        String user_password1 = jPasswordField1.getText();
        String user_password2 = jPasswordField2.getText();
        
        //student details
        String full_name = Stext1.getText();
        String address = Stext2.getText();
        String date_of_birth = year.getSelectedItem().toString() +"-"+ month.getSelectedItem().toString() +"-"+ date.getSelectedItem().toString() ;
        String clas = grade.getSelectedItem().toString() ;
        String phone_no = Stext3.getText();

        //subject details
        String section = jComboBox1.getSelectedItem().toString() ;
        String subjects = Stext4.getText();
        
        if ( user_name1.equals(user_name2) )
        {
            if ( user_password1.equals(user_password2) )
            {
                //insert data to student table
                try
                {
                    String q = "INSERT INTO student (full_name,address,date_of_birth,class,phone_number,section,subjects,user_name,user_password) values ('"+full_name+"','"+address+"','"+date_of_birth+"','"+clas+"','"+phone_no+"','"+section+"','"+subjects+"','"+user_name1+"','"+user_password1+"') ";
                    pst = con.prepareStatement(q);
                    pst.execute();
                    
                    JOptionPane.showMessageDialog(this, "Your Account is Create");
                }
                catch (Exception e)
                {
                    JOptionPane.showMessageDialog(this, e );
                }
                
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Check User Password");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Check User Name");
        }
        
        
    }

