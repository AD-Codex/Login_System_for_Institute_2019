    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String section = jComboBox1.getSelectedItem().toString();
        
        if ( section.equals("A/L section") )
        {
            jComboBox2.removeAllItems();
            jComboBox2.addItem("Combain Maths");
            jComboBox2.addItem("Biology");
            jComboBox2.addItem("Physics");
            jComboBox2.addItem("Chemistry");
            jComboBox2.addItem("Sinhala");
            jComboBox2.addItem("Jeography");
            jComboBox2.addItem("Politics");
        }
        else if ( section.equals("O/L section") )
        {
            jComboBox2.removeAllItems();
            jComboBox2.addItem("Maths");
            jComboBox2.addItem("Science");
            jComboBox2.addItem("Sinhala");
            jComboBox2.addItem("Buddisum");
            jComboBox2.addItem("History");
            jComboBox2.addItem("Jeography");
        }
        else if ( section.equals("Others") )
        {
            jComboBox2.removeAllItems();
        }
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        if ( Stext4.getText().equals("") )
        {
            String subject = jComboBox2.getSelectedItem().toString() ;
            Stext4.setText(subject);
        }
        else
        {
            String text = Stext4.getText() ;   
            String subject = jComboBox2.getSelectedItem().toString() ;
            text = text.concat("\n" + subject);
            Stext4.setText(text);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

